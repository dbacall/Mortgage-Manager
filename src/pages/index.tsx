import { getSession } from "next-auth/react";
import Head from "next/head";
import { type ReactElement, useState, useEffect } from "react";
import { MainLayout } from "src/components";
import type { NextPageWithLayout } from './_app';

const Home: NextPageWithLayout = ({ user, clients }) => {
  const [filteredClients, setFilteredClients] = useState(clients)
  const [searchTerm, setSearchTerm] = useState('')

  useEffect(() => {
    const searchedClients = clients.filter((client) => {
      return `${client.firstName} ${client.lastName}`.toLowerCase().includes(searchTerm.toLowerCase())
    })

    setFilteredClients(searchedClients)
  }, [searchTerm])

  const { company } = user

  console.log('filteredClients', filteredClients);

  return (
    <>
      <Head>
        <title>Mortgage Manager</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="px-10 py-16">
        <h1 className="text-4xl">
          {company.name}
        </h1>
        <div className="mt-6 flex justify-between items-center w-full overflow-none ">
          <h2 className="text-2xl">
            Client Renewals
          </h2>
          <div>
            <input
              type="text"
              placeholder="Searchâ€¦"
              className="input input-bordered border-slate-300"
              onChange={(e) => {
                console.log(e);
                setSearchTerm(e.target.value)

              }}
            />
          </div>
        </div>
        <table className="table w-full mt-5">
          <thead >
            <tr>
              <th className="bg-slate-100">Name</th>
              <th className="bg-slate-100">Phone</th>
              <th className="bg-slate-100">Email</th>
              <th className="bg-slate-100">Renewal Date</th>
            </tr>
          </thead>
          <tbody>
            {filteredClients.map((client, id) => (
              <tr key={id}>
                <td className="border-slate-100">{client.firstName} {client.lastName}</td>
                <td className="border-slate-100">0{client.phone}</td>
                <td className="border-slate-100">{client.email}</td>
                <td className="border-slate-100">{new Date(client.renewalDate).toLocaleDateString('en-GB')}</td>
              </tr>
            ))}

          </tbody>
        </table>
      </main>
    </>
  );
};

export async function getServerSideProps(context) {
  const session = await getSession(context)


  if (!session) {
    return {
      redirect: {
        destination: '/api/auth/signin',
        permanent: false,
      },
    }
  }

  const response = await fetch(`http://localhost:3000/api/user/${session.user.id}`)

  console.log('hereeee 111');
  const user = await response.json()
  console.log('hereeee 222');

  if (!user.companyMembershipId) {
    return {
      redirect: {
        destination: '/getting-started',
        permanent: false,
      },
    }
  }


  const clientsRes = await fetch(`http://localhost:3000/api/mortgageClients/${user.companyId}`)

  const clients = await clientsRes.json()

  console.log('clients server side', clients);

  return {
    props: { user, clients }
  }
}

Home.getLayout = function getLayout(page: ReactElement) {
  return (
    <MainLayout>{page}</MainLayout>
  )
}

export default Home;
