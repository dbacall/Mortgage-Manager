import { getSession } from "next-auth/react";
import Head from "next/head";
import type { ReactElement } from "react";
import { SignedInLayout } from "src/components";
import type { NextPageWithLayout } from './_app';

const Home: NextPageWithLayout = ({ user }) => {
  console.log('home user', user);
  const { company } = user
  return (
    <>
      <Head>
        <title>Mortgage Manager</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="px-10 py-16 ">
        <h1 className="text-4xl">
          {company.name}
        </h1>
      </main>
    </>
  );
};

export async function getServerSideProps(context) {
  const session = await getSession(context)


  if (!session) {
    return {
      redirect: {
        destination: '/api/auth/signin',
        permanent: false,
      },
    }
  }

  const response = await fetch(`http://localhost:3000/api/user/${session.user.id}`)

  const user = await response.json()

  if (!user.companyMembershipId) {
    return {
      redirect: {
        destination: '/getting-started',
        permanent: false,
      },
    }
  }

  return {
    props: { user }
  }
}

Home.getLayout = function getLayout(page: ReactElement) {
  return (
    <SignedInLayout>{page}</SignedInLayout>
  )
}

export default Home;
